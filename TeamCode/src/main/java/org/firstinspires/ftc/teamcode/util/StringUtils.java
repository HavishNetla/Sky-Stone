package org.firstinspires.ftc.teamcode.util;

public class StringUtils {
  public static String caption(String label, String val) {
    return label + ": " + val + "\n";
  }

  public static String caption(String label, double val) {
    return label + ": " + val + "\n";
  }

  public static String caption(String label, boolean val) {
    return label + ": " + val + "\n";
  }
}
